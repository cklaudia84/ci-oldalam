<h4>Játszóterek</h4>
<p>A gyerek attól gyerek, hogy játszik. És ezzel nincs semmi baj. Egy kis rácsodálkozás, egy kis hintázás. Ez így egészséges.  A Szavanna kifutó mellett, frissen felújított, a TÜV szabványainak megfelelő játszóterünkön a kisebbek mászhatnak, csúszhatnak, bújhatnak egy kiadósat.</p>
